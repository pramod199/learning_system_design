package lld.design_inventory_and_order_management;

public class Address {
    String city;
    String state;
    String country;
    String zipCode;

    public Address(String city, String state, String country, String zipCode) {
        this.city = city;
        this.state = state;
        this.country = country;
        this.zipCode = zipCode;
    }
}
